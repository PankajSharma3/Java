//While loop
class main{
    public static void main(String[] args){
        int i=1;
        while(i<11){
            System.out.println(i);
            i++;
        }
    }
}


//Do while loop
class main{
    public static void main(String[] args){
        int i=1;
        do{
            System.out.println(i);
            i++;
        }while(i<11);
    }
}


//For loop
class main{
    public static void main(String[] args){
        for(int i=1;i<11;i++){
            System.out.println(i);
        }
    }
}
